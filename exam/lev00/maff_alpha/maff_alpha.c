#include <unistd.h>

int		main(void)
{
	char a;
	char aa;
	char z;
	char zz;

	a = 'a';
	aa = 'B';
	z = 'z';
	zz = 'Z';
	while(a <= z && aa <= zz)
	{
		write(1, &a, 1);
		write(1, &aa, 1);
		a = a+2;
		aa = aa+2;
	}
	write (1, "\n", 1);
}

