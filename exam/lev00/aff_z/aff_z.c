#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	if(argc != 2)
	{
		write(1, "z\n", 2);
		return (0);
	}
	else 
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] != 'z')
				i++;
			else 
			{				
				write(1, "z", 1);
				break;
			}
		}
	}
	write(1, "\n", 1);
	return (0);
}
