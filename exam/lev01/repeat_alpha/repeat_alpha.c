#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int x;

	i = 0;
	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	else
	{
		while (av[1][i] != '\0')
		{
			if (av[1][i] >= 97 && av[1][i] <= 122)
			{
				x = av[1][i] - 97;
				while (x >= 0)
				{
					write(1, &av[1][i], 1);
					x--;
				}
			}
			else if (av[1][i] >=	65 && av[1][i] <= 90)
			{
				x = av[1][i] - 65;
            	while (x >= 0)
    	    	{
            		write(1, &av[1][i], 1);
            		x--;
    	    	}
			}
			else
				write(1, &av[1][i], 1);
			i++;
		}
		write(1, "\n", 1);
		return (0);
	}
}
