#include "libft.h"

void	ft_putnbr(int n)
{
	char	str[12];
	int		a;
	int		ost;

	a = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n == -2147483648)
	{
		write(1, "2", 1);
		ft_putnbr(147483648);
	}
	while (n >= 1)
	{
		ost = n % 10;
		str[a] = ost + '0';
		n = n / 10;
		a++;
	}
	while (a >= 0)
	{
		write(1, &str[a], 1);
		a--;
	}
}
