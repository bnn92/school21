#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	i = i + 1;
	while (i > 0)
	{
		if (*s == c)
			return ((char *)s);
		++s;
		--i;
	}
	return (0);
}
